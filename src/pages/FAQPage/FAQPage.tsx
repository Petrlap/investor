export const FAQ: React.FC = () => {
  const [openId, setOpenId] = useState<number | null>(null);

  const toggle = (id: number) => {
    setOpenId(openId === id ? null : id);
  };

  return (
    <section className={styles.faqSection}>
      <h1 className={styles.faqTitle}>Часто задаваемые вопросы - FAQ.</h1>
      {faqData.map(({ id, question, answer }) => (
        <div key={id} className={styles.item}>
          <button className={styles.header} onClick={() => toggle(id)}>
            <span>
              {id}. {question}
            </span>
            {openId === id ? (
              <LuCircleChevronUp className={styles.icon} />
            ) : (
              <LuCircleChevronDown className={styles.icon} />
            )}
          </button>
          <div
            className={`${styles.content} ${openId === id ? styles.open : ""}`}
          >
            {answer}
          </div>
        </div>
      ))}
      <ContactForm />
    </section>
  );
};
